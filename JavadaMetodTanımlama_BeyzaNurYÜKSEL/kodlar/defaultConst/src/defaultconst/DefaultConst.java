
package defaultconst;
//default constructor
public class DefaultConst {
      private String ad;
      private String soyad;
      private int yas;
      
      public DefaultConst(String ad, String soyad,int yas){
      
          this.ad = ad;
          this.soyad = soyad;
          this.yas = yas;
      
      }
      public DefaultConst(){
      }
      
      public String getAd(){
      return ad;
      }
      public void setAd(String ad){
      this.ad = ad;
      }
      public String getSoyad(){
      return soyad;
      }
      public void setSoyad(String soyad){
      this.soyad = soyad;
      }
      public int getYas(){
      return yas;
      }
      public void setYas(int yas){
      this.yas= yas;
      }
      
      
      
      
}
 





